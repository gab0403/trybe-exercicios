let processoSeletivo = 'aprovada';

switch (processoSeletivo) {
    case "aprovada":
        console.log("Parabéns, você foi aprovada!");
        break;
        case "reprovada":
            console.log("Você foi reprovada.");
            break;
            case "lista":
                console.log("Você está na lista.");
                break;
                default:
                    console.log("não se aplica.");
}
